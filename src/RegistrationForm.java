    private void proceedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedActionPerformed
        // TODO add your handling code here:
        
        Connection myconn = null;
        Statement mystmt = null;
        ResultSet myrs = null;
        
        try {
            myconn = DriverManager.getConnection("jdbc:mysql://localhost:3306/mumbaihomes","root","root");
        } catch (SQLException ex) {
            Logger.getLogger(Mainform.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try
        {
            validatePassword();       
                        
            String q = "insert into login(USERNAME, PASSWORD, EMAILID) values(?,?,?)";
            PreparedStatement p = myconn.prepareStatement(q);
            p.setString(1, usernamefield.getText());
            p.setString(2, cpassfield.getText());
            p.setString(3, emailaddfield.getText());
                       
            p.executeUpdate();
            
        }
        catch(SQLException e)
        {
            System.out.println(e);
        }
                
        if(fullnamefield.getText().trim().isEmpty() || emailaddfield.getText().trim().isEmpty() || usernamefield.getText().trim().isEmpty() || npassfield.getText().trim().isEmpty() || cpassfield.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "One or more fields are empty");
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Account creation successful.");
            new LoginFrame().setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_proceedActionPerformed

