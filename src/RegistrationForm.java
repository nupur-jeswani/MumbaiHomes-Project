    public void validatePassword()
    {
        String npass = npassfield.getText();
        String cpass = cpassfield.getText(); 
        
        if(!npass.equals(cpass))
        {
            JOptionPane.showMessageDialog(this, "Passwords don't match");
            
            npassfield.setText("");
            cpassfield.setText("");
        }
    }
    
    
